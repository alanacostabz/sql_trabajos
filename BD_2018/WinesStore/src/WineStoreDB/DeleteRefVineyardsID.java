    private void idvKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idvKeyReleased
        // TODO add your handling code here:
        String viñedoid = idv.getText();
        jLabel5.setText("");
        vnombre.setText("");
        rnombre.setText("");
      
        if (!viñedoid.equals("")) {
            try {
                if (validacionDatosC(viñedoid) != 0) {
                    jLabel5.setText("ID valido.");
                    String sql = "SELECT vineyard_code, vineyard_name, region_name, winestore.ref_countries.country_name FROM winestore.ref_vineyards \n"
                            + "inner join winestore.ref_regions \n"
                            + "on winestore.ref_vineyards.region_code = winestore.ref_regions.region_code \n"
                            + "inner join winestore.ref_countries\n"
                            + "on winestore.ref_countries.country_code = winestore.ref_regions.country_code\n"
                            + "where vineyard_code = " + viñedoid;

                    ResultSet rs = db.query(sql);
                    while (rs.next()) {
                        viñedon = rs.getString(2);
                        regionn = rs.getString(3);
                        paisnn = rs.getString(4);

                    }
                    vnombre.setText(viñedon);
                    rnombre.setText(regionn + ", "+paisnn);
                    

                } else {
                    jLabel5.setText("ID no valido.");
                    vnombre.setText("");
                    rnombre.setText("");
                    
                   
                
                }
            } catch (SQLException ex) {
                Logger.getLogger(DeleteRefVineyardsID.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_idvKeyReleased


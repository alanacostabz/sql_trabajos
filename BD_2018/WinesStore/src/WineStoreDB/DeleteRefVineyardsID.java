    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = idv.getText();
        String n = vnombre.getText();
        
        if (!id.isEmpty()) {
            int input = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de eliminar a el viñedo " + n + " con ID = " + id + "?", "Borrar viñedo", 0);
            if (input == 0) {
                String sql = String.format("delete from winestore.ref_vineyards where vineyard_code =%s", id);
                
                try {
                    int value = db.update(sql);
                    if (value == 0) {
                        JOptionPane.showMessageDialog(rootPane, "El viñedo no existe ó ya ha sido eliminado antes.");
                        idv.setText(""); 
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "El viñedo "+ n + " con id=" + id + " ha sido borrado exitosamente.");
                        vnombre.setText("");
                        idv.setText("");
                        rnombre.setText("");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(DeleteRefVineyardsID.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Verifique que el campo de ID que no este vacio y sea valido.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void idvKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idvKeyTyped
        // TODO add your handling code here:
        int limit = 7;
        char c = evt.getKeyChar();
        if ((c < '0' || c > '9') && c != KeyEvent.VK_BACK_SPACE) {
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Solo se pueden ingresar números.");
        }
        if (idv.getText().length() == limit) {
            evt.consume();
            getToolkit().beep();
        }
    }//GEN-LAST:event_idvKeyTyped
    public int validacionDatosC(String vd) throws SQLException {
        StringBuilder sql1 = new StringBuilder("select vineyard_code from winestore.ref_vineyards");
        int x = 0;
        try {
            ResultSet rs = db.query(sql1.toString());
            while (rs.next()) {

                String name = rs.getString(1);

                if (name.equalsIgnoreCase(vd)) {
                    x++;
                }

            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        return x;
    }


    private void jMenuItem6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem6ActionPerformed
//        final String sql = "SELECT cof_name AS Nombre, "
//                + "sup_id AS Proveedor, "
//                + "price AS Precio, "
//                + "sales AS Ventas,"
//                + "total AS Total FROM coffees ORDER BY cof_name";

        final String sql = "SELECT cof_name, sup_id , price , "
                + "sales, total FROM coffees ORDER BY cof_name";

        final String labels[] = {"Nombre", "Proveedor", "Precio",
            "Ventas", "Total"};
        try {
            ResultSet rs = db.query(sql);

            JDBCTableAdpater modelo = new JDBCTableAdpater(rs, labels);

            modelo.addTableModelListener(new CoffeesTableListener(db));

            TableBrowser browser = new TableBrowser("Cafés", modelo);
            browser.setVisible(true);
            this.desktopPane.add(browser);
            rs.close();
        } catch (SQLException ex) {
            System.out.println( ex.getMessage());
        }
    }//GEN-LAST:event_jMenuItem6ActionPerformed


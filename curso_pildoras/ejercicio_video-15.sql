DROP TABLE IF EXISTS PRUEBA;
 CREATE TABLE PRUEBA (
   ID_ALUMNO INT AUTO_INCREMENT,
   NOMBRE VARCHAR(20),
   APELLIDO VARCHAR(20),
   EDAD TINYINT,
   FECHA_NACIMIENTO DATE,
   CARNET BOOL,
   PRIMARY KEY(ID_ALUMNO)
 );

